#include "minishell.h"

void	free_all(t_info *info)
{
	t_env	*tmp;
	int		i;

	while (info->env_list)
	{
		if (info->env_list->key)
			free(info->env_list->key);
		if (info->env_list->value)
			free(info->env_list->value);
		tmp = info->env_list;
		info->env_list = info->env_list->next;
		free(tmp);
	}
	i = -1;
	while (info->token && info->token[++i])
		free(info->token[i]);
	free(info->token);
}

