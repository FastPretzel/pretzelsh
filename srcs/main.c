#include "minishell.h"

void	free_all(t_info *info)
{
	t_env	*tmp;
	int		i;

	while (info->env_list)
	{
		if (info->env_list->key)
			free(info->env_list->key);
		if (info->env_list->value)
			free(info->env_list->value);
		tmp = info->env_list;
		info->env_list = info->env_list->next;
		free(tmp);
	}
	i = -1;
	while (info->token && info->token[++i])
		free(info->token[i]);
	free(info->token);
}

int	main(int argc, char **argv, char **envp)
{
	t_info	info;
	/*int	i = -1;*/

	info.envp = envp;
	/*while (envp[++i])*/
		/*printf("%s\n", envp[i]);*/
	info.token = init_args(argc, argv);
	info.env_list = init_env(envp);
	/*init_builtins(&info);*/
	/*init_blt_names(&info);*/
	execute(&info);
	free_all(&info);
	return (0);
}
